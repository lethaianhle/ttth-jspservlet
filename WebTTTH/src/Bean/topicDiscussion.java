package Bean;

public class topicDiscussion {
	private int discussiontopic_id;
	private String discussiontopic_name;
	private int discussionthread_id;
	
	public topicDiscussion() {}
	
	public int getDiscussiontopic_id() {
		return discussiontopic_id;
	}
	public void setDiscussiontopic_id(int discussiontopic_id) {
		this.discussiontopic_id = discussiontopic_id;
	}
	public String getDiscussiontopic_name() {
		return discussiontopic_name;
	}
	public void setDiscussiontopic_name(String discussiontopic_name) {
		this.discussiontopic_name = discussiontopic_name;
	}
	public int getDiscussionthread_id() {
		return discussionthread_id;
	}
	public void setDiscussionthread_id(int discussionthread_id) {
		this.discussionthread_id = discussionthread_id;
	}
	
	
}
